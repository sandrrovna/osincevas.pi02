def sum_digits(n):
    """
    Рекурсивная функция для вычисления суммы цифр числа.
    
    Args:
        n (int): Целое число (может быть отрицательным)
    
    Returns:
        int: Сумма цифр числа
    """
    # Приводим число к положительному, так как знак не влияет на сумму цифр
    n = abs(n)
    
    # Базовый случай: если число состоит из одной цифры
    if n < 10:
        return n
    
    # Рекурсивный случай: сумма последней цифры и суммы остальных цифр
    return (n % 10) + sum_digits(n // 10)



# Примеры использования функции
if __name__ == "__main__":
    # Тестовые случаи
    print(f"sum_digits(123) = {sum_digits(123)}")      # Ожидаемый результат: 6
    print(f"sum_digits(9876) = {sum_digits(9876)}")     # Ожидаемый результат: 30
    print(f"sum_digits(0) = {sum_digits(0)}")            # Ожидаемый результат: 0
    print(f"sum_digits(-456) = {sum_digits(-456)}")   # Ожидаемый результат: 15
    print(f"sum_digits(7) = {sum_digits(7)}")          # Ожидаемый результат: 7
